						<p><span style="margin-left:5em">În</span> anul 1959 a luat ființă „Doina Timișului”, ansamblu care reunește 
							studenți de la aproape toate facultățile din Centrul Universitar din Timișoara, ansamblu care s-a făcut 
							foarte repede cunoscut pentru că și-a luat drept scop să aduca omagiu extrem de variaței culturi 
							tradiționale. Înalta ținută a spectacolelor prezentate, autenticitatea cântecelor și a dansurilor, a 
							tradițiilor și costumelor, aduse pe scena cu mare artă, au adus în puțin timp acestei formații un mare 
							renume atât în țara cît și în străinătate.
							<br><br>
							<span style="margin-left:5em">Partea</span> coregrafică a spectacolelor se îmbină armonios cu cea 
							muzicală, fiind constituită pe aceleași coordonate ca ale unui mozaic reprezentativ pentru toate zonele 
							țării. Partea muzicală a spectacolelor este susținută de o formație orchestrală formată din viori, 
							clarinete, taragot, acordeon, contrabas, chitară, saxofon și instrumente care redau culoarea specifică 
							muzicii populare românești: fluier, drâmbă, ocarină, caval. În spectacolele ansamblului nostru sunt puse 
							în valoare vechi melodii pe cale de dispariție, diverse melodii de ritualuri, foarte melodioase, cu 
							ritmuri variate, antrenante, adesea 	sincopate și de asemenea melodii create și intrate recent în 
							circulație, melodii care exprimă optimismul și bucuria de a trăi care caracterizează sufletul poporului 
							nostru. Repertoriul orchestrei, a interpreților și soliștilor instrumentiști cuprinde melodii din toate 
							zonele țarii: de la doina din Banat la brâul din Muntenia, de la hora din Moldova la învârtita din 
							Transilvania, care constituie un amplu mozaic, reprezentativ și pitoresc al melosului popular românesc.</p>
